const mongoose = require('mongoose')
let Schema = mongoose.Schema

let userSchema = new Schema({
    name: {
        type: String,
        required: true
    },
    username: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    role: {
        type: String,
        required: true
    }
}) // userSchema

let User = mongoose.model('User', userSchema)
module.exports = User